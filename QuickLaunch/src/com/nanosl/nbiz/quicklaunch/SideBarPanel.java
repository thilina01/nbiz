    public void loadButtons() {
        ArrayList<JButton> jButtons = ToolBarManager.getToolBarButtons();
        toolBar.removeAll();
        jButtons.stream().forEach((jButton) -> {
            toolBar.add(jButton);
        });
        toolBar.repaint();
    }
