    private void supplierTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supplierTableMouseClicked
        if (evt.getClickCount() > 1) {
            suppllier = Manager.getInstance().find(Supplier.class, supplierTable.getValueAt(supplierTable.getSelectedRow(), 0).toString());
            dispose();
        }
    }//GEN-LAST:event_supplierTableMouseClicked

    private void searchTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchTextFieldActionPerformed
        if (supplierTable.getRowCount() == 1) {
            suppllier = Manager.getInstance().find(Supplier.class, supplierTable.getValueAt(0, 0).toString());
            dispose();
        }
    }//GEN-LAST:event_searchTextFieldActionPerformed

    private void searchTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTextFieldKeyReleased
        if (evt.getKeyCode() == 27) {
            dispose();
            return;
        } else if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            if (supplierTable.getRowCount() > 0) {
                supplierTable.requestFocus();
                supplierTable.setRowSelectionInterval(0, 0);
                return;
            }
        }
        String text = searchTextField.getText().trim();
        supplierTableModel.setRowCount(0);
        if (text.length() > 0) {
            List<Supplier> suppliers = Find.supplierBy$(text);
            for (Supplier supplier : suppliers) {
                Object[] row = {supplier.getCode(), supplier.getName(), supplier.getCity()};
                supplierTableModel.addRow(row);
            }
        }
    }//GEN-LAST:event_searchTextFieldKeyReleased

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        TopComponent tc = WindowManager.getDefault().findTopComponent("CustomerTopComponent");
        if (tc != null) {
            tc.open();
            tc.requestActive();
        }
        dispose();
    }//GEN-LAST:event_addButtonActionPerformed

